package com.ntt.smartpbx.model.data;

/**
 *
 * 名称: VmNotifyData class.
 *
 */
public class VmNotifyData {
    /** The Vm Id */
    private String vmId;
    /** The N Number Name */
    private String n_number_name;
    /** The Ch Num*/
    private int ch_num;
    /** The Vm Dicide Under */
    private int vm_dicide_under;
    /** The Vm Dicide Top */
    private int vm_dicide_top;
    /** The n number info id */
    private long n_number_info_id;


    /**
     * Default constructor
     */
    public VmNotifyData(){
        super();
    }

    /**
     * @return vmId
     */
    public String getVmId() {
        return vmId;
    }

    /**
     * @param vmId the vmId to set
     */
    public void setVmId(String vmId) {
        this.vmId = vmId;
    }

    /**
     * @return n_number_name
     */
    public String getN_number_name() {
        return n_number_name;
    }

    /**
     * @param n_number_name the n_number_name to set
     */
    public void setN_number_name(String n_number_name) {
        this.n_number_name = n_number_name;
    }

    /**
     * @return ch_num
     */
    public int getCh_num() {
        return ch_num;
    }

    /**
     * @param ch_num the ch_num to set
     */
    public void setCh_num(int ch_num) {
        this.ch_num = ch_num;
    }

    /**
     * @return vm_dicide_under
     */
    public int getVm_dicide_under() {
        return vm_dicide_under;
    }

    /**
     * @param vm_dicide_under the vm_dicide_under to set
     */
    public void setVm_dicide_under(int vm_dicide_under) {
        this.vm_dicide_under = vm_dicide_under;
    }

    /**
     * @return vm_dicide_top
     */
    public int getVm_dicide_top() {
        return vm_dicide_top;
    }

    /**
     * @param vm_dicide_top the vm_dicide_top to set
     */
    public void setVm_dicide_top(int vm_dicide_top) {
        this.vm_dicide_top = vm_dicide_top;
    }

    /**
     * @return n_number_info_id
     */
    public long getN_number_info_id() {
        return n_number_info_id;
    }

    /**
     * @param n_number_info_id the n_number_info_id to set
     */
    public void setN_number_info_id(long n_number_info_id) {
        this.n_number_info_id = n_number_info_id;
    }
}
